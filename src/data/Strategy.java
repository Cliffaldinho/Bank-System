package data;

public class Strategy {

	private int effectiveness;
	private int improvementFromSituationBefore;
	private int practical;
	private int relevanceToIncident;
	private int satisfactionOfStrategy;
	
	public Strategy() {
		
	}
	
	public int getEffectiveness() {
		return effectiveness;
	}

	public void setEffectiveness(int effective) {
		this.effectiveness = effective;
	}

	public int getPractical() {
		return practical;
	}

	public void setPractical(int prac) {
		this.practical = prac;
	}

	public int getImprovementFromSituationBefore() {
		return improvementFromSituationBefore;
	}

	public void setImprovementFromSituationBefore(int improvement) {
		this.improvementFromSituationBefore = improvement;
	}

	public int getRelevanceToIncident() {
		return relevanceToIncident;
	}

	public void setRelevanceToIncident(int relevance) {
		this.relevanceToIncident = relevance;
	}

	public int getSatisfactionOfStrategy() {
		return satisfactionOfStrategy;
	}

	public void setSatisfactionOfStrategy(int satisfaction) {
		this.satisfactionOfStrategy = satisfaction;
	}

	
	
	
	
}
